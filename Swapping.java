public class Main {
  public static void main(String[] args) {
    int a=95,b=85,temp;
    System.out.println("Before Swapping a= "+a);
    System.out.println("After Swapping b= "+b);
    temp=a;
    a=b;
    b=temp;
    System.out.println("Before Swapping a= "+a);
    System.out.println("After Swapping b= "+b);
  }
}
